void    ft_putchar(char c);

void    ft_putnbr(int nb)
{
int sign;

sign = 1;
if (nb < 0)
{
ft_putchar('-');
sign = -1;
}
if (nb / 10 != 0)
ft_putnbr(nb / 10 * sign);
ft_putchar((nb % 10) * sign + 48);
}
